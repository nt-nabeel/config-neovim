local opt = vim.opt
opt.signcolumn = "yes"
opt.swapfile = false

-- number line
opt.number = true
opt.relativenumber = true

-- tab & indent
opt.tabstop = 2
opt.shiftwidth = 2
opt.softtabstop = 2
opt.expandtab = true
opt.autoindent = true

-- search settings
opt.ignorecase = true
opt.smartcase = true

-- window split
opt.splitbelow = true
opt.splitright = true
